class EscapeSequences {
	public static void main(String args[]) {
		//add a tab
		System.out.println("Luminar\tTechnolab");
		//add a line
		System.out.println("Luminar\nTechnolab");
		//add a single quote
		System.out.println("Luminar\'Technolab");
		//add a double quote
		System.out.println("Luminar\"Technolab");
		//add a backslash
		System.out.println("Luminar\\Technolab");
	}

}