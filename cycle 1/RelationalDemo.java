class RelationalDemo {
	public static void main(String args[]) {
		int numOne=7;
		int numTwo=20;
		System.out.println("numOne is "+numOne+" and numTwo is "+numTwo);
		System.out.println("numOne == numTwo-> "+(numOne==numTwo));
		System.out.println("numOne != numTwo-> "+(numOne!=numTwo));
		System.out.println("numOne > numTwo-> "+(numOne>numTwo));
		System.out.println("numOne < numTwo-> "+(numOne<numTwo));
		System.out.println("numOne >= numTwo-> "+(numOne>=numTwo));
		System.out.println("numOne <= numTwo-> "+(numOne<=numTwo));

	}

}