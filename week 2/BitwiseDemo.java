class BitwiseDemo {
	public static void main(String args[]) {
		int numOne=12;
		int numTwo=25;
		int numThree=5;
		int numFour=5;
		System.out.println(numOne & numTwo);
		System.out.println(numOne | numTwo);
		System.out.println(numOne ^ numTwo);
		System.out.println(numThree << 2);
		System.out.println(numFour >> 3);
	}

}
